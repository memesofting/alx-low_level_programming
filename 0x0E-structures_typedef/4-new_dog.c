#include <stdio.h>
#include <stdlib.h>
#include "dog.h"

/**
 * init_dog - initialises variable type struct dog
 * @d: struct object
 * @name: dog name
 * @age: dog age
 * @owner: dog owner
 *
 * Return: void
 */

dog_t *new_dog(char *name, float age, char *owner)
{
	if (d != NULL)
	{
		d->name = name;
		d->age = age;
		d->owner = owner;
	}
}
